def test_sync_phase_checkpoints_preserves_unmanaged_markdown_and_cleans_generated_outputs_when_nothing_remains(
    tmp_path: Path,
) -> None:
    cwd = _setup_project(tmp_path)
    (cwd / ".gpd" / "phases" / "01-empty-phase").mkdir()

    checkpoint_dir = cwd / ".gpd" / "phase-checkpoints"
    checkpoint_dir.mkdir()
    generated = checkpoint_dir / "01-empty-phase.md"
    _write_generated_checkpoint(generated, "Phase 01 Checkpoint")
    unmanaged = checkpoint_dir / "notes.md"
    unmanaged.write_text("# keep me\n", encoding="utf-8")
    root_index = cwd / ".gpd" / "CHECKPOINTS.md"
    _write_generated_checkpoint(root_index, "Phase Checkpoints")

    result = sync_phase_checkpoints(cwd)

    assert result.phase_count == 0
    assert result.preserved_phase_count == 0
    assert ".gpd/phase-checkpoints/01-empty-phase.md" in result.removed_files
    assert ".gpd/CHECKPOINTS.md" in result.removed_files
    assert generated.exists() is False
    assert unmanaged.exists()
    assert unmanaged.read_text(encoding="utf-8") == "# keep me\n"
    assert root_index.exists() is False


def test_sync_phase_checkpoints_keeps_existing_generated_outputs_for_failed_phase_and_cleans_empty_dir(
    tmp_path: Path,
) -> None:
    cwd = _setup_project(tmp_path)
    (cwd / ".gpd" / "phases" / "01-bad-phase").mkdir()
    (cwd / ".gpd" / "phases" / "01-bad-phase" / "01-SUMMARY.md").write_bytes(b"\x80\x81\x82")

    checkpoint_dir = cwd / ".gpd" / "phase-checkpoints"
    checkpoint_dir.mkdir()
    preserved = checkpoint_dir / "01-bad-phase.md"
    _write_generated_checkpoint(preserved, "Phase 01 Checkpoint")

    result = sync_phase_checkpoints(cwd)

    assert result.phase_count == 0
    assert result.preserved_phase_count == 1
    assert result.generated is True
    assert preserved.exists()
    assert ".gpd/phase-checkpoints/01-bad-phase.md" not in result.removed_files
    assert (cwd / ".gpd" / "CHECKPOINTS.md").exists()
