"""Tests for generated human-facing phase checkpoint documents."""

from __future__ import annotations

import textwrap
from pathlib import Path

from gpd.core.checkpoints import sync_phase_checkpoints

_GENERATED_HEADER = "<!-- Generated by gpd sync-phase-checkpoints. Do not edit by hand. -->"


def _setup_project(tmp_path: Path) -> Path:
    planning = tmp_path / ".gpd" / "phases"
    planning.mkdir(parents=True)
    return tmp_path


def _write_summary(
    path: Path,
    *,
    phase: str,
    plan: str,
    one_liner: str,
    completed: str = "2026-03-17",
    provides: str | list[str] = "phase checkpoint",
) -> None:
    lines = [
        "---",
        f'phase: "{phase}"',
        f'plan: "{plan}"',
        "depth: full",
    ]
    if isinstance(provides, list):
        lines.append("provides:")
        lines.extend(f"  - {item}" for item in provides)
    else:
        lines.append(f"provides: {provides!r}")
    lines.extend(
        [
            f'completed: "{completed}"',
            f'one-liner: "{one_liner}"',
            "key-files:",
            "  - src/model.py",
            "key-decisions:",
            "  - Keep the reduced wedge",
            "patterns-established:",
            "  - Hidden-damage rejection survives",
            "---",
            "",
            "# Summary",
            "",
            f"**{one_liner}**",
            "",
            "## Key Results",
            "",
            "The phase produced a clean checkpoint result.",
            "",
        ]
    )
    path.write_text("\n".join(lines), encoding="utf-8")


def _write_malformed_summary(path: Path, *, phase: str, plan: str) -> None:
    path.write_text(
        textwrap.dedent(
            f"""\
            ---
            phase: "{phase}"
            plan: "{plan}"
            completed: "2026-03-17"
            one-liner: "Broken summary"
            key-files:
              - src/model.py
            key-decisions:
              - Keep the reduced wedge
            patterns-established:
              - Hidden-damage rejection survives
            invalid: [unterminated
            ---

            # Summary
            """
        ),
        encoding="utf-8",
    )


def _write_generated_checkpoint(path: Path, title: str) -> None:
    path.write_text(
        textwrap.dedent(
            f"""\
            {_GENERATED_HEADER}

            # {title}
            """
        ),
        encoding="utf-8",
    )


def test_sync_phase_checkpoints_generates_root_and_phase_docs(tmp_path: Path) -> None:
    cwd = _setup_project(tmp_path)
    phase_dir = cwd / ".gpd" / "phases" / "01-test-phase"
    phase_dir.mkdir()
    _write_summary(phase_dir / "01-SUMMARY.md", phase="01", plan="01", one_liner="Set up the project")
    (phase_dir / "01-VERIFICATION.md").write_text("# Verification\n\nPassed.\n", encoding="utf-8")

    result = sync_phase_checkpoints(cwd)

    assert result.generated is True
    assert result.phase_count == 1
    assert result.preserved_phase_count == 0
    assert ".gpd/phase-checkpoints/01-test-phase.md" in result.updated_files
    assert ".gpd/CHECKPOINTS.md" in result.updated_files

    phase_checkpoint = (cwd / ".gpd" / "phase-checkpoints" / "01-test-phase.md").read_text(encoding="utf-8")
    assert "# Phase 01 Checkpoint" in phase_checkpoint
    assert "[01-SUMMARY.md](../phases/01-test-phase/01-SUMMARY.md)" in phase_checkpoint
    assert "[01-VERIFICATION.md](../phases/01-test-phase/01-VERIFICATION.md)" in phase_checkpoint
    assert "The headline result was straightforward" in phase_checkpoint

    checkpoints_index = (cwd / ".gpd" / "CHECKPOINTS.md").read_text(encoding="utf-8")
    assert "[Phase 01: Test Phase](phase-checkpoints/01-test-phase.md)" in checkpoints_index
    assert "\\" not in phase_checkpoint
    assert "\\" not in checkpoints_index

    rerun = sync_phase_checkpoints(cwd)
    assert rerun.updated_files == []
    assert rerun.removed_files == []


