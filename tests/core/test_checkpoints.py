def test_sync_phase_checkpoints_skips_malformed_summary_and_still_regenerates_other_phases(
    tmp_path: Path,
) -> None:
    cwd = _setup_project(tmp_path)

    good_phase_dir = cwd / ".gpd" / "phases" / "01-good-phase"
    good_phase_dir.mkdir()
    _write_summary(good_phase_dir / "01-SUMMARY.md", phase="01", plan="01", one_liner="Set up the project")
    (good_phase_dir / "01-VERIFICATION.md").write_text("# Verification\n\nPassed.\n", encoding="utf-8")

    bad_phase_dir = cwd / ".gpd" / "phases" / "02-bad-phase"
    bad_phase_dir.mkdir()
    _write_malformed_summary(bad_phase_dir / "01-SUMMARY.md", phase="02", plan="01")

    result = sync_phase_checkpoints(cwd)

    assert result.phase_count == 1
    assert result.preserved_phase_count == 0
    assert "Skipped .gpd/phases/02-bad-phase/01-SUMMARY.md: malformed frontmatter" in "\n".join(result.errors)
    assert ".gpd/phase-checkpoints/01-good-phase.md" in result.updated_files
    assert ".gpd/CHECKPOINTS.md" in result.updated_files
    assert not (cwd / ".gpd" / "phase-checkpoints" / "02-bad-phase.md").exists()

    good_checkpoint = (cwd / ".gpd" / "phase-checkpoints" / "01-good-phase.md").read_text(encoding="utf-8")
    checkpoints_index = (cwd / ".gpd" / "CHECKPOINTS.md").read_text(encoding="utf-8")

    assert "# Phase 01 Checkpoint" in good_checkpoint
    assert "[01-SUMMARY.md](../phases/01-good-phase/01-SUMMARY.md)" in good_checkpoint
    assert "[Phase 01: Good Phase](phase-checkpoints/01-good-phase.md)" in checkpoints_index
    assert "Phase 02" not in checkpoints_index
    assert "\\" not in good_checkpoint
    assert "\\" not in checkpoints_index


def test_sync_phase_checkpoints_preserves_existing_generated_output_for_unreadable_summary(
    tmp_path: Path,
) -> None:
    cwd = _setup_project(tmp_path)

    good_phase_dir = cwd / ".gpd" / "phases" / "01-good-phase"
    good_phase_dir.mkdir()
    _write_summary(good_phase_dir / "01-SUMMARY.md", phase="01", plan="01", one_liner="Set up the project")
    (good_phase_dir / "01-VERIFICATION.md").write_text("# Verification\n\nPassed.\n", encoding="utf-8")

    bad_phase_dir = cwd / ".gpd" / "phases" / "02-binary-phase"
    bad_phase_dir.mkdir()
    (bad_phase_dir / "01-SUMMARY.md").write_bytes(b"\x80\x81\x82")

    checkpoint_dir = cwd / ".gpd" / "phase-checkpoints"
    checkpoint_dir.mkdir()
    preserved = checkpoint_dir / "02-binary-phase.md"
    _write_generated_checkpoint(preserved, "Phase 02 Checkpoint")

    result = sync_phase_checkpoints(cwd)

    assert result.phase_count == 1
    assert result.preserved_phase_count == 1
    assert "Skipped .gpd/phases/02-binary-phase/01-SUMMARY.md: unreadable or missing file" in "\n".join(result.errors)
    assert preserved.exists()
    assert preserved.read_text(encoding="utf-8").startswith(_GENERATED_HEADER)
    assert ".gpd/phase-checkpoints/02-binary-phase.md" not in result.removed_files
    assert ".gpd/phase-checkpoints/01-good-phase.md" in result.updated_files
    checkpoints_index = (cwd / ".gpd" / "CHECKPOINTS.md").read_text(encoding="utf-8")
    assert "[Phase 02: Binary Phase](phase-checkpoints/02-binary-phase.md)" in checkpoints_index


